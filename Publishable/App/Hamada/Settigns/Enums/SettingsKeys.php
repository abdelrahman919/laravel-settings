<?php 

namespace App\Hamada\Settings\Enums;


/**
 * Enum SettingsKeys
 *
 * This enum represents the keys used for application settings.
 * Eliminates the need for hardcoded strings throughout the application.
 *
 * @method static array getValues() Returns an array of all enum case values.
 */
enum SettingsKeys: string
{

    /**
     * Example enum cases:
     * case APP_NAME = 'app_name';
     */ 



    public static function getValues(): array
    {
        return array_map(fn($case) => $case->value, self::cases());
    }
}